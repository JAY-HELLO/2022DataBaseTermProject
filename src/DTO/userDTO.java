public class userDTO {
    Integer ussn;
    String name;
    String phone;

    public Integer getUssn() {
        return ussn;
    }

    public void setUssn(Integer ussn) {
        this.ussn = ussn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

}
